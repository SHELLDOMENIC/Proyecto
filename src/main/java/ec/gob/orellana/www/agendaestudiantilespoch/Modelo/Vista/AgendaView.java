public class AgendaView extends JFrame {

    private AgendaController controller;

    public AgendaView(AgendaController controller) {
        this.controller = controller;
        setTitle("Agenda Estudiantil ESPOCH");
        setSize(600, 400);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        initComponents();

    }

    public void initComponents() {
        JPanel panel = new JPanel();
        panel.setLayout(new GridLayout(5, 1, 10, 10));

        JButton btnMateria = new JButton("Agregar Materia");
        JButton btnTarea = new JButton("Agregar Tarea");
        JButton btnExamen = new JButton("Agregar Examen");
        JButton btnResumen = new JButton("Ver Resumen");

        btnMateria.addActionListener(e -> mostrarFormularioMateria());
        btnTarea.addActionListener(e -> mostrarFormularioTarea());
        btnExamen.addActionListener(e -> mostrarFormularioExamen());
        btnResumen.addActionListener(e -> mostrarResumen());

        panel.add(btnMateria);
        panel.add(btnTarea);
        panel.add(btnExamen);
        panel.add(btnResumen);

        add(panel);
    }

    public void mostrarFormularioMateria() {
        String nombre = JOptionPane.showInputDialog("Nombre materia:");
        String docente = JOptionPane.showInputDialog("Docente:");
        String horario = JOptionPane.showInputDialog("Horario:");
        String aula = JOptionPane.showInputDialog("Aula:");
        controller.agregarMateria(new Materia(nombre, docente, horario, aula));
    }

    public void mostrarFormularioTarea() {
        String descripcion = JOptionPane.showInputDialog("Descripci√≥n de tarea:");
        String fecha = JOptionPane.showInputDialog("Fecha:");
        controller.agregarTarea(new Tarea(descripcion, fecha));
    }

    public void mostrarFormularioExamen() {
        String materia = JOptionPane.showInputDialog("Materia del examen:");
        String fecha = JOptionPane.showInputDialog("Fecha del examen:");
        String tema = JOptionPane.showInputDialog("Tema:");
        controller.agregarExamen(new Examen(materia, fecha, tema));
    }

    public void mostrarResumen() {
        JTextArea textArea = new JTextArea(controller.listarResumen());
        textArea.setEditable(false);
        JScrollPane scrollPane = new JScrollPane(textArea);
        scrollPane.setPreferredSize(new Dimension(500, 300));
        JOptionPane.showMessageDialog(this, scrollPane, "Resumen General", JOptionPane.INFORMATION_MESSAGE);
    }
}

